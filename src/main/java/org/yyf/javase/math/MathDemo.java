public class MathDemo {
    public static void main(String[] args) {
        Double d = 123.9d;
        double d2 = -123.9d;
        double d3 = -123.1d;
        double AfterFloor = Math.floor(d);
        double AfterFloor2 = Math.floor(d2);
        double AfterFloor3 = Math.floor(d3);
        System.out.println(AfterFloor);
        System.out.println(AfterFloor2);
        System.out.println(AfterFloor3);
    }


}
